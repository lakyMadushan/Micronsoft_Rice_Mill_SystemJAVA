    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.search("select * from r_bag_creator where r_type_bag like '%" + jTextField3.getText() + "%'");
            while (rs.next()) {
                //Vector v = new Vector();
                Vector<Object> v = new Vector<>();
                v.add(rs.getString("id"));
                v.add(rs.getString("r_type_bag"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("date"));
                v.add(rs.getString("time"));
                dtm.addRow(v);

                int sum = 0;
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    ResultSet rs2 = DB.search("select * from r_bag_creator where r_type_bag = '" + dtm.getValueAt(i, 1).toString() + "' ");
                    if (rs2.next()) {
                        sum = sum + Integer.parseInt(dtm.getValueAt(i, 2).toString());
                    }
                    jLabel11.setText(String.valueOf(sum));
                }

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField3KeyReleased

