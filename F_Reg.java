    private void jTextField5KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField5KeyReleased
        try {
            if (jRadioButton1.isSelected()) {

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);

                ResultSet rs = DB.search("select * from f_reg where f_name like '%" + jTextField5.getText() + "%' ");
                while (rs.next()) {
                    Vector<Object> v = new Vector<>();
                    v.add(rs.getString("f_id"));
                    v.add(rs.getString("f_name"));
                    v.add(rs.getString("tp"));
                    v.add(rs.getString("adrs"));
                    dtm.addRow(v);
                }

                ResultSet rs1 = DB.search("select * from f_reg where f_name like '%" + jTextField5.getText() + "%'  ");
                Vector v1 = new Vector();
                while (rs1.next()) {
                    if (!rs1.getString("f_name").equals(jTextField5.getText())) {
                        v1.add(rs1.getString("f_id") + ":" + rs1.getString("f_name") + "-" + rs1.getString("tp") + "-" + rs1.getString("adrs"));
                    }
                }
                jList1.setListData(v1);
            }
            if (jRadioButton2.isSelected()) {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                ResultSet rs = DB.search("select * from f_reg where tp like '%" + jTextField5.getText() + "%' ");
                while (rs.next()) {
                    Vector<Object> v = new Vector<>();
                    v.add(rs.getString("f_id"));
                    v.add(rs.getString("f_name"));
                    v.add(rs.getString("tp"));
                    v.add(rs.getString("adrs"));

                    dtm.addRow(v);
                }
                ResultSet rs1 = DB.search("select * from f_reg where tp like '%" + jTextField5.getText() + "%'");
                Vector v1 = new Vector();
                while (rs1.next()) {
                    if (!rs1.getString("tp").equals(jTextField5.getText())) {
                        v1.add(rs1.getString("f_id") + ":" + rs1.getString("f_name") + "-" + rs1.getString("tp") + "-" + rs1.getString("adrs"));
                    }
                }
                jList1.setListData(v1);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTextField5KeyReleased

