    public void tableload() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.search("SELECT * FROM f_reg");
            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("f_id"));
                v.add(rs.getString("f_name"));
                v.add(rs.getString("tp"));
                v.add(rs.getString("adrs"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void idload() {
        try {
            ResultSet rs = DB.search("SELECT `f_id` FROM f_reg ORDER BY `f_id` DESC");
            if (rs.next()) {
                String id = rs.getString("f_id");
                int id1 = Integer.parseInt(id) + 1;
                jTextField1.setText(String.valueOf(id1));

            } else {
                jTextField1.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

