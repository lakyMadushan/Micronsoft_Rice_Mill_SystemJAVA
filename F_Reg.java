    private void jTextField4KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyReleased
        // TODO add your handling code here:
        if (jTextField3.getText().length() == 10) {
            if (jTextField3.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Phone Number!", "ERROR", JOptionPane.ERROR_MESSAGE);
                jTextField4.setText("");
                jTextField3.requestFocus();
            } else {

                char letter = evt.getKeyChar();
                if (Character.isLetter(letter) | Character.isDigit(letter) | evt.getKeyCode() == KeyEvent.VK_SPACE | evt.getKeyCode() == KeyEvent.VK_PERIOD
                        | evt.getKeyCode() == KeyEvent.VK_BACK_SPACE | evt.getKeyCode() == KeyEvent.VK_CAPS_LOCK | evt.getKeyCode() == KeyEvent.VK_SHIFT
                        | evt.getKeyCode() == KeyEvent.VK_DELETE | evt.getKeyCode() == KeyEvent.VK_BACK_SLASH | evt.getKeyCode() == KeyEvent.VK_NUM_LOCK
                        | evt.getKeyCode() == KeyEvent.VK_BACK_QUOTE | evt.getKeyCode() == KeyEvent.VK_COMMA | evt.getKeyCode() == KeyEvent.VK_ENTER
                        | evt.getKeyCode() == KeyEvent.VK_RIGHT | evt.getKeyCode() == KeyEvent.VK_LEFT) {
                    jTextField4.setEditable(true);
                } else {
                    jTextField4.setEditable(false);
                    JOptionPane.showMessageDialog(this, "Use only letters,space,dot,backspace,shift,back flash,num lock or caps lock.", "ERROR!", JOptionPane.ERROR_MESSAGE);
                    jTextField4.setText("");

                }

            }

        } else {
            JOptionPane.showMessageDialog(this, "Please Enter Ivalid Phone Number!", "ERROR", JOptionPane.ERROR_MESSAGE);
            jTextField3.requestFocus();

        }

    }//GEN-LAST:event_jTextField4KeyReleased

